public class FragmentAnim {
    protected int mEnter;
    protected int mExit;
    protected int mPopExit;
    protected int mPopEnter;


    public int getEnter() {
        return mEnter;
    }


    public void setEnter(int enter) {
        mEnter = enter;
    }


    public int getExit() {
        return mExit;
    }


    public void setExit(int exit) {
        mExit = exit;
    }


    public int getPopExit() {
        return mPopExit;
    }


    public void setPopExit(int popExit) {
        mPopExit = popExit;
    }


    public int getPopEnter() {
        return mPopEnter;
    }


    public void setPopEnter(int popEnter) {
        mPopEnter = popEnter;
    }
}
